package models.enums;

/**
 * Type of information used to describe a Study.
 *
 */
public enum InfoType {
	
   /**
    * a URL to a external resource
    */
   URL,
   
   /**
    * an email address
    */
   EMAIL,
   
   /**
    * a phone number
    */
   PHONE,
   
   /**
    * a text giving some piece of information about the study
    */
   TEXT
}
