package models.enums;

/**
 * how often should an action be done?
 *
 */
public enum Frequency {

  /**
   * the action should be done once
   */
  ONCE,
  
  /**
   * the action should be done every day
   */
  DAILY,
  
  /**
   * the action should be done once a week
   */
  WEEKLY,
  
  /**
   * the action should be done once a month
   */
  MONTHLY,
  
  /**
   * the action should be done once a year
   */
  YEARLY
}
